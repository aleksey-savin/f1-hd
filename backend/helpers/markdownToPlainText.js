// Снимает базовый Markdown-синтаксис, оставляя чистый текст для глобального
// поиска (plainText). Лёгкий regex-стриппер без внешних зависимостей.
module.exports.markdownToPlainText = (markdown = "") => {
  if (!markdown || typeof markdown !== "string") {
    return "";
  }

  return (
    markdown
      // блоки кода ```...``` и ~~~...~~~
      .replace(/```[\s\S]*?```/g, " ")
      .replace(/~~~[\s\S]*?~~~/g, " ")
      // инлайн-код `...`
      .replace(/`([^`]*)`/g, "$1")
      // изображения ![alt](url) -> alt
      .replace(/!\[([^\]]*)\]\([^)]*\)/g, "$1")
      // ссылки [text](url) -> text
      .replace(/\[([^\]]*)\]\([^)]*\)/g, "$1")
      // заголовки в начале строки (#, ##, ...)
      .replace(/^\s{0,3}#{1,6}\s+/gm, "")
      // цитаты >
      .replace(/^\s{0,3}>\s?/gm, "")
      // маркеры списков (-, *, +, 1.) в начале строки
      .replace(/^\s*([-*+]|\d+\.)\s+/gm, "")
      // разделители таблиц и горизонтальные линии
      .replace(/^\s*\|?[\s:|-]+\|?\s*$/gm, " ")
      // вертикальные палки таблиц -> пробел
      .replace(/\|/g, " ")
      // эмфазис/зачёркивание ** * __ _ ~~
      .replace(/(\*\*|\*|__|_|~~)/g, "")
      // HTML-теги, если вдруг есть
      .replace(/<[^>]+>/g, " ")
      // схлопываем пробелы
      .replace(/\s+/g, " ")
      .trim()
  );
};
